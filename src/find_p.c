#include "../include/so_long.h"

void	find_p(char **map, t_point *P)
{
	int			i;
	int			j;

	i = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			if (map[i][j] == 'P')
			{
				P->x = j;
				P->y = i;
				return ;
			}
			j++;
		}
		i++;
	}
}
